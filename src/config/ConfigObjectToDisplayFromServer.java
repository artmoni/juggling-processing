package config;

public class ConfigObjectToDisplayFromServer {

	int size;
	String texture;
	int color;

	
	public ConfigObjectToDisplayFromServer() {
	}


	public int getSize() {
		return size;
	}


	public void setSize(int size) {
		this.size = size;
	}


	public String getTexture() {
		return texture;
	}


	public void setTexture(String texture) {
		this.texture = texture;
	}


	public int getColor() {
		return color;
	}


	public void setColor(int color) {
		this.color = color;
	}

	
	
}
